package com.loops.forloop;

public class Evenumbers {
  public static void main(String []args) {
	  int numbers = 100; 
	  System.out.println("Even numbers of "+numbers+":");
	  
	  
	  for(int i=1; i<=numbers; i++) 
	  {
		  if(i%2==0)
		  {
			  System.out.println(i+" ");
		  }
	  }
  }
}
	  
  

